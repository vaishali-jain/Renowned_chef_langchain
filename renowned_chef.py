from dotenv import load_dotenv

load_dotenv()

from langchain.tools import tool
from typing import Dict, Any
from tavily import TavilyClient

tavily_client = TavilyClient()

@tool
def web_search(query: str) -> Dict[str, Any]:

    """Search the web for information"""

    return tavily_client.search(query)

system_prompt = """

You are a renowned chef. The user will give you a list of ingredients they have left over in their house. They can also give image of vegetables, fruits or any edible item

Using the web search tool, search the web for recipes that can be made with the ingredients they have.

Return recipe suggestions and the recipe instructions to the user, if requested.

"""

from langchain.agents import create_agent

agent = create_agent(
    model="gpt-5-nano",
    tools=[web_search],
    system_prompt=system_prompt
)